function isVowel(char) {
  if (char.toLowerCase() === 'a') {
    return true;
  }
  if (char.toLowerCase() === 'e') {
    return true;
  }
  if (char.toLowerCase() === 'i') {
    return true;
  }
  if (char.toLowerCase() === 'o') {
    return true;
  }
  if (char.toLowerCase() === 'u') {
    return true;
  }
  return false;
}
